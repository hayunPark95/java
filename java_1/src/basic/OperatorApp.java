package basic;

public class OperatorApp {
	public static void main(String[] args) {
		System.out.println("10+20*3 = "+(10+20*3));
		System.out.println("(10+20)*3 = "+((10+20)*3));
		
		int a1=10;
		System.out.println("a1 = "+a1);
		System.out.println("-a1 = "+(-a1));
		
		System.out.println("20>10 = "+(20>10));
		System.out.println("!20>10 = "+!(20>10));
	}
}
